"""다양한 시나리오의 테스트용 임대차 계약서 PDF 생성."""

import os
from pathlib import Path
from fpdf import FPDF

PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
OUTPUT_DIR = PROJECT_ROOT / "data"

FONT_PATHS = [
    "/System/Library/Fonts/AppleSDGothicNeo.ttc",
    "/System/Library/Fonts/Supplemental/AppleGothic.ttf",
    "/Library/Fonts/NanumGothic.ttf",
]


def find_korean_font():
    for path in FONT_PATHS:
        if os.path.exists(path):
            return path
    return None


def build_pdf(filename, title, parties, clauses, date):
    """공통 PDF 생성 함수."""
    pdf = FPDF()
    pdf.set_auto_page_break(auto=True, margin=15)

    font_path = find_korean_font()
    if font_path:
        pdf.add_font("Korean", "", font_path, uni=True)
        font = "Korean"
    else:
        font = "Helvetica"

    pdf.add_page()

    # 제목
    pdf.set_font(font, size=18)
    pdf.cell(0, 15, title, align="C", new_x="LMARGIN", new_y="NEXT")
    pdf.ln(10)

    # 당사자
    pdf.set_font(font, size=10)
    pdf.multi_cell(0, 7, parties)
    pdf.ln(5)

    # 조항
    for clause_title, content in clauses:
        pdf.set_font(font, size=12)
        pdf.cell(0, 10, clause_title, new_x="LMARGIN", new_y="NEXT")
        pdf.set_font(font, size=10)
        pdf.multi_cell(0, 7, content)
        pdf.ln(3)

    # 서명
    pdf.ln(10)
    pdf.set_font(font, size=10)
    pdf.cell(0, 7, date, align="C", new_x="LMARGIN", new_y="NEXT")

    out = OUTPUT_DIR / filename
    pdf.output(str(out))
    print(f"생성 완료: {out}")


