"""DOCX 계약서 샘플 생성 스크립트.

임대차/근로/매매 계약서 샘플을 생성한다. 각 샘플에는 의도적으로 독소 조항을
포함하여 분석기의 탐지 동작을 검증할 수 있도록 한다.

사용법:
  python -m backend.scripts.make_docx_samples

출력 경로: data/samples/*.docx
"""

from pathlib import Path
from docx import Document
from docx.shared import Pt

OUTPUT_DIR = Path("data/samples")


def _new_document(title: str) -> Document:
  doc = Document()
  style = doc.styles["Normal"]
  style.font.name = "맑은 고딕"
  style.font.size = Pt(11)

  heading = doc.add_heading(title, level=0)
  heading.alignment = 1  # center
  return doc


def _add_clauses(doc: Document, clauses: list[tuple[str, str]]) -> None:
  for article, body in clauses:
    p = doc.add_paragraph()
    run = p.add_run(article)
    run.bold = True
    doc.add_paragraph(body)
    doc.add_paragraph("")


